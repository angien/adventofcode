import java.io.File
import java.util.Scanner

/* 
 * count number of times there is an increase
 */

class Solution(val inputStrings: List<String>) {
	fun solution(): Int {
		var count = 0
		var checkAgainst = Integer.MAX_VALUE

		inputStrings.forEach {
			val value = it.toInt()
			if (value > checkAgainst) {
				count += 1
			}
			checkAgainst = value
		}
		return count
	}
}

