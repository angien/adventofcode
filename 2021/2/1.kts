import java.io.File
import java.util.Scanner

class Solution(val inputStrings: List<String>) {
	fun solution(): Int {
		var depth = 0
		var horizontal = 0

		inputStrings.forEach {
			val direction = it.split(" ")[0]
			val value = it.split(" ")[1].toInt()

			when (direction) {
				"forward" -> horizontal += value
				"down" -> depth += value
				"up" -> depth -= value
			}
		}
		return depth * horizontal
	}
}

